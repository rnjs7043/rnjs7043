package kr.co.koitt;

public class SearchVO {

	private String searchDeptNo;
	private String searchJob;
	private String searchName;

	public String getSearchDeptNo() {
		return searchDeptNo;
	}
	public void setSearchDeptNo(String searchDeptNo) {
		this.searchDeptNo = searchDeptNo;
	}
	public String getSearchJob() {
		return searchJob;
	}
	public void setSearchJob(String searchJob) {
		this.searchJob = searchJob;
	}
	public String getSearchName() {
		return searchName;
	}
	public void setSearchName(String searchName) {
		this.searchName = searchName;
	}

}//class
